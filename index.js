function somando(numero1, numero2){
    return numero1 + numero2;
}

const resultadoSoma = somando(25, 25);
console.log(resultadoSoma);

function multiplicando(numero1, numero2){
    return numero1 * numero2;
}

const resultadoMultiplicacao = multiplicando(25, 25);
console.log(resultadoMultiplicacao);

function subtraindo(numero1, numero2){
    return numero1 - numero2;
}

const resultadoSubtracao = subtraindo(25, 25);
console.log(resultadoSubtracao);

function dividindo(numero1, numero2){
    return numero1 / numero2;
}

const resultadoDivisao = dividindo(25, 25);
console.log(resultadoDivisao);